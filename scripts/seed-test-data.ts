const toLocalDayISO = (input: Date | string): string => {
  const date = typeof input === 'string' ? new Date(input) : new Date(input)
  date.setHours(0, 0, 0, 0)
  return date.toISOString()
}

const atLocalTimeISO = (input: Date | string, hours: number, minutes: number): string => {
  const date = typeof input === 'string' ? new Date(input) : new Date(input)
  date.setHours(hours, minutes, 0, 0)
  return date.toISOString()
}

const addDays = (input: Date | string, days: number): Date => {
  const date = typeof input === 'string' ? new Date(input) : new Date(input)
  const next = new Date(date)
  next.setDate(next.getDate() + days)
  return next
}

const formatYmd = (input: Date | string): string => {
  const date = typeof input === 'string' ? new Date(input) : new Date(input)
  const year = date.getFullYear()
  const month = `${date.getMonth() + 1}`.padStart(2, '0')
  const day = `${date.getDate()}`.padStart(2, '0')
  return `${year}${month}${day}`
}

const weekdaysInLastNDays = (days: number): Date[] => {
  const list: Date[] = []
  const now = new Date()

  for (let i = days; i >= 1; i -= 1) {
    const day = addDays(now, -i)
    const weekday = day.getDay()

    if (weekday === 0 || weekday === 6) {
      continue
    }

    list.push(day)
  }

  return list
}

